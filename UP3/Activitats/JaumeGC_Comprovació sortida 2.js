//Concatenació String i number
console.log("5" + 5);
// Sortida: "55"
// Tipus: string
// Explicació: el + amb un string fa que el number es converteixi automàticament a string.

//Resta de String i number
console.log("5" - 5);
// Sortida: 0
// Tipus: number
// Explicació: amb operadors matemàtics com - el string es converteix a number.

//Tipus de dades dels valors
console.log(typeof "5"); // Sortida: "string"
console.log(typeof 5);   // Sortida: "number"

//Suma d’un booleà i un number
console.log(true + 5);
// Sortida: 6
// Tipus: number
// Explicació: true es converteix automàticament a 1, false a 0.